package Catalog.Struct;

public class FurnitureStruct {
    public String name_Tabl;
    public String tip_Tabl;
    public String layout_Tabl;
    public int height_Tabl;
    public int width_Tabl;
    public int length_Tabl;
    public String filler_Tabl;
    public String upholstery_Tabl;
    public String accessories_Tabl;
    public Double price_Tabl;

    public Double getPrice_Tabl() {
        return price_Tabl;
    }

    public void setPrice_Tabl(Double price) {
        this.price_Tabl = price;
    }

    public String getName_Tabl() {
        return name_Tabl;
    }

    public void setName_Tabl(String name_Tabl) {
        this.name_Tabl = name_Tabl;
    }

    public String getTip_Tabl() {
        return tip_Tabl;
    }

    public void setTip_Tabl(String tip_Tabl) {
        this.tip_Tabl = tip_Tabl;
    }

    public String getLayout_Tabl() {
        return layout_Tabl;
    }

    public void setLayout_Tabl(String layout_Tabl) {
        this.layout_Tabl = layout_Tabl;
    }

    public int getHeight_Tabl() {
        return height_Tabl;
    }

    public void setHeight_Tabl(int height_Tabl) {
        this.height_Tabl = height_Tabl;
    }

    public int getWidth_Tabl() {
        return width_Tabl;
    }

    public void setWidth_Tabl(int width_Tabl) {
        this.width_Tabl = width_Tabl;
    }

    public int getLength_Tabl() {
        return length_Tabl;
    }

    public void setLength_Tabl(int length_Tabl) {
        this.length_Tabl = length_Tabl;
    }

    public String getFiller_Tabl() {
        return filler_Tabl;
    }

    public void setFiller_Tabl(String filler_Tabl) {
        this.filler_Tabl = filler_Tabl;
    }

    public String getUpholstery_Tabl() {
        return upholstery_Tabl;
    }

    public void setUpholstery_Tabl(String upholstery_Tabl) {
        this.upholstery_Tabl = upholstery_Tabl;
    }

    public String getAccessories_Tabl() {
        return accessories_Tabl;
    }

    public void setAccessories_Tabl(String accessories_Tabl) {
        this.accessories_Tabl = accessories_Tabl;
    }

    public FurnitureStruct(String name_Tabl, String tip_Tabl, String layout_Tabl, int height_Tabl, int width_Tabl, int length_Tabl, String filler_Tabl, String upholstery_Tabl, String accessories_Tabl, Double price_Tabl) {
        this.name_Tabl = name_Tabl;
        this.tip_Tabl = tip_Tabl;
        this.layout_Tabl = layout_Tabl;
        this.height_Tabl = height_Tabl;
        this.width_Tabl = width_Tabl;
        this.length_Tabl = length_Tabl;
        this.filler_Tabl = filler_Tabl;
        this.upholstery_Tabl = upholstery_Tabl;
        this.accessories_Tabl = accessories_Tabl;
        this.price_Tabl = price_Tabl;
    }
}
